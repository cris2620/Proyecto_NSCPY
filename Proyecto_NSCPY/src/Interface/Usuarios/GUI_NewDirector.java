        jcbPerCod.removeAllItems();
        jcbPerNomCom.removeAllItems();
        jcbPerDNI.removeAllItems();
        
        for (String[] date : codPer) {
            jcbPerCod.addItem(date[0]);
            jcbPerNomCom.addItem(date[1] + " " + date[2] + " " + date[3]);
            jcbPerDNI.addItem(date[4]);
        }
        myConect.close();
        
